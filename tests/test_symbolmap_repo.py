""" Tests for symbol map repository """
from pricedb.repositories import SymbolMapRepository

def test_loading_by_id(session):
    """ Find the map by id """
    repo = SymbolMapRepository(session)
    element = repo.get_by_id("AEF.AX")

    assert element
    assert element.in_symbol == "AEF.AX"
    assert element.out_symbol == "ASX:AEF"
